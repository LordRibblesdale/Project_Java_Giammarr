public class Digital extends Clock {
  Digital() {
    super();
  }

  Digital(int hrs) {
    super(hrs);
  }

  Digital(int hrs, int min) {
    super(hrs, min);
  }

  Digital(int hrs, int min, int sec) {
    super(hrs, min, sec);
  }

  public String toString() {
    return super.toString();
  }
}
